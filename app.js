/* メイン率が編集されたら、内訳の各率を等比スケールして積を一致させる */
function scaleSubToHead(sub, targetPct){
  const cur = prod(sub)*100;
  if(cur<=0) return;
  const factor = Math.pow((targetPct/100)/(cur/100), 1/sub.length);
  sub.forEach(s=>{ s.rate = Math.min(100, Math.max(1, +(s.rate*factor).toFixed(1))); });
}
